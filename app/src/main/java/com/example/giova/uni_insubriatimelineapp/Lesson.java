package com.example.giova.uni_insubriatimelineapp;

public class Lesson {

    private String nameLesson;
    private String classRoom;
    private String facolta;
    private String timeStart;
    private String timeFinish;
    private String lessonsInformation;

    public String getNameLesson() {
        return nameLesson;
    }

    public void setNameLesson(String nameLesson) {
        this.nameLesson = nameLesson;
    }

    public String getClassRoom() {
        return classRoom;
    }

    public void setClassRoom(String classRoom) {
        this.classRoom = classRoom;
    }

    public String getFacolta() {
        return facolta;
    }

    public void setFacolta(String facolta) {
        this.facolta = facolta;
    }

    public String getTimeStart() {
        return timeStart;
    }

    public void setTimeStart(String timeStart) {
        this.timeStart = timeStart;
    }

    public String getTimeFinish() {
        return timeFinish;
    }

    public void setTimeFinish(String timeFinish) {
        this.timeFinish = timeFinish;
    }

    public String getLessonsInformation() {
        return lessonsInformation;
    }

    public void setLessonsInformation(String lessonsInformation) {
        this.lessonsInformation = lessonsInformation;
    }
}
